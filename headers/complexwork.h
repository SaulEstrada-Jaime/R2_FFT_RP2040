//estructura del numero complejo
typedef struct {
    float Re;
    float Im;
} complex;
//Aproximacion del valor de la magnitud del numero complejo, dado del uso restringido de los punto flotante
//Se opta por aproximar la magnitud a un numero entero
int magnitude(complex num) {
    int A = (int)sqrt(num.Re * num.Re + num.Im * num.Im);
    return A;}
//funcion para la multiplicacion de dos numeros complejos
complex dot_complex(complex fstwn, complex sndwn) {
    complex result;
    result.Re = (fstwn.Re * sndwn.Re) - (fstwn.Im * sndwn.Im);
    result.Im = (fstwn.Re * sndwn.Im) + (fstwn.Im * sndwn.Re);
    return result;
}
complex sum_complex(complex fstwn, complex sndwn){
    complex res;
    res.Re = fstwn.Re + sndwn.Re;
    res.Im = fstwn.Im + sndwn.Im;
    return res;
}
